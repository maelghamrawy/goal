import UIKit

extension UIButton {
    
    func setSelectColor() {
        self.backgroundColor = #colorLiteral(red: 0.4274509804, green: 0.737254902, blue: 0.3882352941, alpha: 1)
    }
    
    func serDeselectcolor() {
        self.backgroundColor = #colorLiteral(red: 0.7646081365, green: 1, blue: 0.6354379737, alpha: 1)
    }
}
